public class AartiBucket {

    String num,
    aartiName;

    public AartiBucket() {
    }

    public AartiBucket(String num, String aartiName) {
        this.num = num;
        this.aartiName = aartiName;
    }

    public String getNum() {
        return num;
    }

    public String getAartiName() {
        return aartiName;
    }
}
